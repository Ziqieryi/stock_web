// sidebar js
function openNav() {
    document.getElementById("mySidebar").style.width = "175px";
    document.getElementById("main").style.marginLeft = "175px";
}

/* 设置侧边栏宽度为0，页面内容左边距为0 */
function closeNav() {
    document.getElementById("mySidebar").style.width = "0";
    document.getElementById("main").style.marginLeft = "0";
}
